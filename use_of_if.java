class use_of_if{
  public static void main(String[] args) {
    // create a string variable
    String language = "C++";

    // if statement
    if (language == "Java") {
      System.out.println("Best Programming Language");
    }
    else if(language == "C++"){
        System.out.println("Higher level language");

    }
    else{
        System.out.println("Something wrong with code");
    }
  }
}